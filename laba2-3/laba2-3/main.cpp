#include <iostream>
using namespace std;

int main() {
    locale::global(locale("ru_RU.UTF-8"));

    // Вывод с использованием цикла while
    cout << "=== Цикл while ===" << endl;
    double i = 10.0; // Начальное значение
    while (i <= 25.0) { // Условие продолжения цикла
        cout << i << " " << i + 0.4 << endl; // Вывод текущего числа и числа + 0.4
        i += 1.0; // Увеличение счетчика на 1
    }
    cout << endl; // Разделитель

    // Вывод с использованием цикла do-while
    cout << "=== Цикл do-while ===" << endl;
    i = 10.0; // Сброс значения для нового цикла
    do {
        cout << i << " " << i + 0.4 << endl; // Вывод текущего числа и числа + 0.4
        i += 1.0; // Увеличение счетчика на 1
    } while (i <= 25.0); // Условие продолжения цикла
    cout << endl; // Разделитель

    // Вывод с использованием цикла for
    cout << "=== Цикл for ===" << endl;
    for (double j = 10.0; j <= 25.0; j += 1.0) { // Инициализация, условие, шаг
        cout << j << " " << j + 0.4 << endl; // Вывод текущего числа и числа + 0.4
    }

    return 0;
}