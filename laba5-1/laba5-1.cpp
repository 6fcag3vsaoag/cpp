#include <iostream>
#include <string> // Для работы со строками
#include <locale> // Для поддержки русской локализации

using namespace std;

int main() {
    // Устанавливаем русскую локализацию
    setlocale(LC_ALL, "Russian");

    // Объявляем две строки
    string str1 = "кошка";
    string str2 = "кошка";

    // Сравниваем строки
    if (str1 == str2) {
        // Если строки равны, выводим сообщение об этом
        cout << "Строки равны: \"кошка\" == \"кошка\"" << endl;
        // Обоснование:
        // Строки "кошка" и "кошка" состоят из одинаковых символов в одинаковом порядке.
        // В C++ оператор == для строк сравнивает их посимвольно.
        // Если все символы совпадают, строки считаются равными.
    }
    else {
        // Если строки не равны, выводим сообщение об этом
        cout << "Строки не равны: \"кошка\" != \"кошка\"" << endl;
    }

    return 0;
}