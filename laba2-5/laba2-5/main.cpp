#include <iostream>
#include <cmath> // Для sqrt()
using namespace std;

int main() {
    // Установка русской локали для корректного вывода русских символов
    setlocale(LC_ALL, "Russian"); // Устанавливаем глобальную локаль

    double a, b, h; // Интервал [a, b] и шаг h

    // Ввод данных пользователем
    cout << "Введите начало интервала (a): ";
    cin >> a;
    cout << "Введите конец интервала (b): ";
    cin >> b;
    cout << "Введите шаг (h): ";
    cin >> h;

    // Проверка корректности шага
    if (h <= 0) {
        cout << "Шаг должен быть положительным числом!" << endl;
        return 1; // Завершение программы с ошибкой
    }

    cout << "\nТаблица значений функции y = sqrt(x^2 - 1):\n";
    cout << "------------------------------------------\n";

    // Цикл для перебора значений x на интервале [a, b]
    for (double x = a; x <= b; x += h) {
        cout << "x = " << x << ": ";

        // Проверка, определена ли функция
        if (x * x - 1 >= 0) {
            double y = sqrt(x * x - 1); // Вычисление значения функции
            cout << "y = " << y << endl;
        }
        else {
            cout << "Функция не определена" << endl; // Сообщение об ошибке
        }
    }

    return 0;
}