#include <iostream>
#include <string>
#include <windows.h> // Для работы с кодировкой в Windows
// Данный заголовочный файл подключается для использования функций API операционной системы Windows,
// необходимых для настройки и управления аспектами вывода символов и текста на консоль.
//
// Конкретно этот файл позволяет установить правильную кодировку (например, UTF-8 или Windows-1251),
// благодаря чему символы кириллицы будут отображаться и считываться корректно в окне терминала Windows.
//
// Если программа выводит русский текст, использование #include <windows.h> вместе с соответствующими функциями,
// такими как SetConsoleOutputCP(), обеспечивает правильное отображение русских букв и специальных символов.


using namespace std;

int main() {
    // Устанавливаем русскую локализацию для консоли
    SetConsoleCP(1251); // Устанавливаем кодировку ввода (Windows-1251)
    SetConsoleOutputCP(1251); // Устанавливаем кодировку вывода (Windows-1251)
    setlocale(LC_ALL, "Russian");

    // Вводим строку
    string input;
    cout << "Введите строку: ";
    getline(cin, input);

    // Определяем гласные буквы кириллицы (строчные и заглавные)
    string vowels = "аеёиоуыэюяАЕЁИОУЫЭЮЯ";

    // Счетчик гласных
    int vowelCount = 0;

    // Проходим по каждому символу строки
    for (char ch : input) {
        // Проверяем, является ли символ гласной
        if (vowels.find(ch) != string::npos) {
            vowelCount++;
        }
    }

    // Выводим результат
    cout << "Количество гласных букв в строке: " << vowelCount << endl;

    return 0;
}